import math

if __name__ == "__main__":
    radius = float(input("Enter radius: "))
    _area_of_circle_ = math.pi * radius * radius
    _circum_of_circle_ = 2 * math.pi * radius
    print("Radius of circle: {price:.2f}".format(price=radius))
    print("Area of circle: {_area_of_circle_:.2f}".format(_area_of_circle_=_area_of_circle_))
    print("Circumference of circle: {_circum_of_circle_:.2f}".format(_circum_of_circle_=_circum_of_circle_))
